fun main(){
    val n1:Int = 546
    val n2:Int = 113
    println("$n1 maior que $n2?${n1>n2}")
    println("$n1 menor que $n2?${n1<n2}")
    println("$n1 maior ou igual a $n2?${n1>=n2}")
    println("$n1 menor ou igual a $n2?${n1<=n2}")
    println("$n1 é igual a $n2?${n1==n2}")
    println("$n1 é diferente de $n2?${n1!=n2}")
}